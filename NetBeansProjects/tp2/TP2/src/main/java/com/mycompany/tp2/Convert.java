    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
       
        double temperature = Double.parseDouble(TEM.getText());

       
        String conversionType = (String) jComboBox1.getSelectedItem();

      
        double resultFahrenheit = 0.0;
        double resultCelsius = 0.0;
        double resultKelvins = 0.0;
       

       
        switch (conversionType) {
            case "Fahrenheit TO Celsius":
                resultCelsius = (temperature - 32) * 5 / 9;
                jLabel3.setText(""+temperature); 
                jLabel4.setText(" " + String.format("%.2f", resultCelsius));
                break;
            case "Celsius TO Fahrenheit":
                resultFahrenheit = temperature * 9 / 5 + 32;
                jLabel4.setText(" " + String.format("%.2f", resultFahrenheit));
                jLabel3.setText("" +temperature); 
                break;
            case "kelvins TO Fahrenheit":
                resultFahrenheit = 1.8 * (temperature - 273) + 32;
                jLabel4.setText("" + String.format("%.2f", resultFahrenheit));
                jLabel3.setText("" +temperature); 
                break;
            case "kelvins TO Celsius":
                resultCelsius = temperature - 273;
                jLabel3.setText("" +temperature);
                jLabel4.setText(" " + String.format("%.2f", resultCelsius));
                break;
            default:
             
                JOptionPane.showMessageDialog(this, "SÃ©lection non valide", "Erreur", JOptionPane.ERROR_MESSAGE);
                return; 
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
      String selectedOption = (String) jComboBox1.getSelectedItem();
    switch (selectedOption) {
        case "Fahrenheit TO Celsius":
            jLabel2.setText("Fahrenheit");
            jLabel1.setText("Celsius");
            break;
        case "Celsius TO Fahrenheit":
            jLabel2.setText("Celsius");
            jLabel1.setText("Fahrenheit");
            break;
        case "kelvins TO Fahrenheit":
            jLabel2.setText("Kelvins");
            jLabel1.setText("Fahrenheit");
            
            break;
        case "kelvins TO Celsius":
            jLabel2.setText("Kelvins");
          
            jLabel1.setText("Celsius");
            
            break;
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

