private void populateTable() {
        try {
            List<Book> books = bookService.getAllBooks(); 
            DefaultTableModel model = (DefaultTableModel) bookTable.getModel();
            model.setRowCount(0);
            for (Book book : books) {
                Object[] row = {
                    book.getTitle(),
                    book.getAuthor(),
                    book.getIsbn(),
                    book.getLanguage(),
                    book.getGenre(),
                    book.getPublisher(),
                    book.getYear(),
                    book.getCopies()
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

